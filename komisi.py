HARGA_BALA_BALA = 2000
HARGA_CIRENG = 1000
HARGA_PISANG_GORENG = 2500

jml_bala2 = float(input("Jumlah beli bala-bala : "))
jml_cireng = float(input("Jumlah beli cireng : "))
jml_pisang_goreng = float(input('Jumlah beli pisang goreng : '))

harga_total_bala2 = HARGA_BALA_BALA * jml_bala2
harga_total_cireng = HARGA_CIRENG * jml_cireng
harga_total_pisang_goreng = HARGA_PISANG_GORENG * jml_pisang_goreng

total = harga_total_bala2 + harga_total_cireng + harga_total_pisang_goreng
diskon = total * 10/100
bayar = total -diskon

print('Harga beli : ', str(total))
print('Diskon : ', str(diskon))
print('Jumlah Bayar : ', str(bayar))




